package com.example.saeed.booklistingapp;

public class BookListing {

    private String mAuthor;
    private String mTitle;

    public BookListing(String author, String title) {
        mAuthor = author;
        mTitle = title;
    }

    public String getmAuthor() {
        return mAuthor;
    }
    public String getmTitle() {
        return mTitle;
    }
}


